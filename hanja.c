tINT Hj2HgComp( tHCHAR *Hj, HANJA_TABLE *HanjaTable )
{
	return (tINT)(*Hj - HanjaTable->Hj);
}


tHCHAR Hj2Hg( tHCHAR Hj )
{
        HANJA_TABLE *p;

        p = (HANJA_TABLE *)bsearch(&Hj, HanjaTable, COUNT_HANJA, sizeof(HANJA_TABLE), (CompProcP)Hj2HgComp);
        if(p == NULL) {
                return(Hj);
        }
	else {
                return(p->Hg);
        }
}


tBOOL HjStr2HgStr( tHCHAR *HjStr, tHCHAR *HgStr )
{
        tINT i;
        tBOOL bRetVal = FALSE;

        for(i = 0; i < HStrlen(HjStr); i++) {
                if(IsHanja_KSSM(HjStr[i])) {
                        bRetVal = TRUE;
                        HgStr[i] = Hj2Hg(HjStr[i]);
                }else {
                        HgStr[i] = HjStr[i];
                }
        }
	HgStr[i] = 0x00;
        return(bRetVal);
}

